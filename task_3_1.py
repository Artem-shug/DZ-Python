

def num_translate(x):
    if x == "zero":
        print('"ноль"')
    elif x == "one":
        print('"один"')
    elif x == "two":
        print('"два"')
    elif x == "three":
        print('"три"')
    elif x == "four":
        print('"четыре"')
    elif x == "five":
        print('"пять"')
    elif x == "six":
        print('"шесть"')
    elif x == "seven":
        print('"семь"')
    elif x == "eight":
        print('"восемь"')
    elif x == "nine":
        print('"девять"')
    elif x == "ten":
        print('"десять"')
    else:
        print("None")
    return x

num_translate("zero")

